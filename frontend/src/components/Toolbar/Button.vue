<template>

  <!-- 
    在其他元件中使用這個 <base-button> 的時候，可以透過參數 property控制button內容跟格式
    
    1:  link 決定這個 button 是一般的 <button>
        還是 可以透過 vue router 轉跳頁面的 <router-link> 
    2:            
  -->
  <button v-if="!link" :class="mode">
    <slot></slot>
  </button>
  <router-link v-else :to="to" :class="mode">
    <slot></slot>
  </router-link>


</template>

<script>
export default {
  // 檢查使用 component 的時候要傳入的參數和預設值
  props: {
    mode: {
      type: String,
      required: false,
      default: null
    },
    link: {
      type: Boolean,
      required: false,
      default: false
    },
    to: {
      type: String,
      required: false,
      default: '/'
    }
  }
}
</script>

